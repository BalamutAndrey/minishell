#include "libft.h"

void	ft_puterrornbr(int n)
{
	size_t	nn;

	nn = 0;
	if (n < 0)
	{
		ft_puterror('-');
		if (n == -2147483648)
		{
			n = n + 1;
			nn = 1;
		}
		n = n * -1;
	}
	if (n > 9)
	{
		ft_puterrornbr(n / 10);
		ft_puterror(((n + nn) % 10) + 48);
	}
	else
	{
		ft_puterror((n + nn) + 48);
	}
}
