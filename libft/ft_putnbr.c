#include "libft.h"

void	ft_putnbr(int n)
{
	size_t	nn;

	nn = 0;
	if (n < 0)
	{
		ft_putchar('-');
		if (n == -2147483648)
		{
			n = n + 1;
			nn = 1;
		}
		n = n * -1;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putchar(((n + nn) % 10) + 48);
	}
	else
	{
		ft_putchar((n + nn) + 48);
	}
}
