#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f) (t_list *elem))
{
	t_list	tmp;

	tmp = *lst;
	while (lst != NULL)
	{
		(*f)(lst);
		lst = lst->next;
	}
}
